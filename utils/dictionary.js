/**
 * Get statistics about the dictionary distribution
 * @returns {object} Statistics object with en/ett counts
 */
function getDictionaryStats() {
  const enWords = dictionary.filter(entry => entry.article === "en").length;
  const ettWords = dictionary.filter(entry => entry.article === "ett").length;
  
  return {
    total: dictionary.length,
    en: enWords,
    ett: ettWords,
    enPercentage: ((enWords / dictionary.length) * 100).toFixed(1),
    ettPercentage: ((ettWords / dictionary.length) * 100).toFixed(1)
  };
}

